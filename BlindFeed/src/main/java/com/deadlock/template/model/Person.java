package com.deadlock.template.model;

/*
 * model class for the person
 * contain firstname last name and age
 * */ 

public class Person {
	String fname;
	String lname;
	int age;
	
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getLname() {
		return lname;
	}
	public void setLname(String lname) {
		this.lname = lname;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
